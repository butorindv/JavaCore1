public class Fruit {
    private float weight;
    private String name = "Fruit";


    public void setName(String name) {
        this.name = name;
    }

    public Fruit() {

    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public String getName() {
        return name;
    }


}
